-- Refactor length_services: consolidate boolean flags into nullable numeric columns.
-- Old: multiple rows per (length_catalog_id, team_service_id) distinguished by is_miter_time/is_cutout_time
-- New: one row per (length_catalog_id, team_service_id) with time_per_unit, miter_minutes, cutout_minutes, base_minutes
--
-- This is a destructive migration — existing length_services data will be lost.
-- Acceptable since lengths feature is in early stages.

-- 1. Drop the old table (cascade removes RLS policies, indexes, etc.)
DROP TABLE IF EXISTS public.length_services CASCADE;

-- 2. Recreate with new schema
CREATE TABLE public.length_services (
  id bigint generated by default as identity not null,
  created_at timestamp with time zone not null default now(),
  updated_at timestamp with time zone null default now(),
  length_catalog_id bigint not null,
  team_service_id bigint not null,
  time_per_unit numeric(10, 4) null,      -- minutes per linear foot
  miter_minutes numeric(10, 4) null,      -- minutes per miter
  cutout_minutes numeric(10, 4) null,     -- minutes per cutout
  base_minutes numeric(10, 4) null,        -- minimum minutes per service per length item
  constraint length_services_pkey primary key (id),
  constraint length_services_unique unique (length_catalog_id, team_service_id),
  constraint length_services_length_catalog_id_fkey foreign key (length_catalog_id)
    references lengths_catalog (id) on update cascade on delete cascade,
  constraint length_services_team_service_id_fkey foreign key (team_service_id)
    references team_services (id) on update cascade on delete cascade
) tablespace pg_default;

comment on table public.length_services is 'Per-service labor config for each length catalog item. One row per (catalog item, service).';
comment on column public.length_services.time_per_unit is 'Minutes per linear foot for this service';
comment on column public.length_services.miter_minutes is 'Minutes per miter cut for this service (null = no miter labor)';
comment on column public.length_services.cutout_minutes is 'Minutes per cutout for this service (null = no cutout labor)';
comment on column public.length_services.base_minutes is 'Minimum minutes charged for this service regardless of length (null = no minimum)';

-- 3. Re-add RLS using has_team_permission for can_manage_teams
-- - All team members can select length services for their team.
-- - Only users with team permission "can_manage_teams" can insert/update/delete
--   (role_id=1 admins are allowed by has_team_permission baseline).
ALTER TABLE public.length_services ENABLE ROW LEVEL SECURITY;

CREATE POLICY "Team members can view their length services"
  ON public.length_services FOR SELECT
  USING (
    EXISTS (
      SELECT 1
      FROM public.lengths_catalog lc
      JOIN public.team_members tm ON tm.team_id = lc.team_id
      WHERE lc.id = length_services.length_catalog_id
        AND tm.user_id = auth.uid()
    )
  );

CREATE POLICY "Team members can insert length services"
  ON public.length_services FOR INSERT
  WITH CHECK (
    public.has_team_permission(
